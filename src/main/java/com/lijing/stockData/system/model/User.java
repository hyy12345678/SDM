package com.lijing.stockData.system.model;

public class User {
	private String userName;
	private String age;
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getUserName() {
		return userName;
	}
	public void setAge(String age) {
		this.age = age;
	}
	public String getAge() {
		return age;
	}

}
